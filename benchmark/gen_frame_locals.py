#!/usr/bin/env python3
# Generate a frame-local (param) read throughput workload.
#
# A `{ |a b| ... }` frame proc whose TOP-LEVEL body is K unrolled
# `a b add pop` sequences, driven N times by an outer `repeat`.  Only a
# frame proc's own depth-0 top-level executable name-refs are rewritten to
# slot-refs by the scanner (nested procs stay dynamic name lookups), so the
# unrolled top-level reads exercise the slot-ref read path on a slot-indexing
# build and the dict-stack name-lookup path on a pre-slot-indexing build
# (baseline 1b41bf0f).  The outer driver is `repeat`, not recursion, so there
# is no TCO-depth dependency and the per-call begin-locals/end is amortized
# over 4*K dispatches.
#
# Param reads per call = 2*K.  Calls = N.  Total param reads = 2*K*N.
# Output is written next to this script as bench_frame_locals.trx.
import os

K = 200  # unrolled `a b add pop` per call (4*K body elements; scan-stack cap ~1023)
N = 125000  # outer repeat count

READS_PER_LINE = 10  # `a b add pop` groups per source line

here = os.path.dirname(os.path.abspath(__file__))
out = os.path.join(here, "bench_frame_locals.trx")

lines = []
lines.append("% bench_frame_locals.trx -- frame-local (param) read throughput.")
lines.append("%")
lines.append("% GENERATED by gen_frame_locals.py -- do not edit by hand.")
lines.append("%")
lines.append("% `hot` is a |a b| frame proc whose top-level body reads its params")
lines.append("% a and b 2*K times (K balanced `a b add pop`).  Only top-level frame-")
lines.append("% name refs are slot-indexed, so these reads use the slot-ref path on a")
lines.append("% slot-indexing build vs a dict-stack name lookup on the baseline.")
lines.append("% The outer `repeat` drives N calls (no recursion -> no TCO depth).")
lines.append("%")
lines.append(f"% Param reads = 2*K*N = 2 * {K} * {N} = {2 * K * N}.")
lines.append("")
lines.append("/hot {")
lines.append("  |a b|")

group = "a b add pop"
full_lines = K // READS_PER_LINE
remainder = K % READS_PER_LINE
for _ in range(full_lines):
    lines.append("  " + "  ".join([group] * READS_PER_LINE))
if remainder:
    lines.append("  " + "  ".join([group] * remainder))

lines.append("} def")
lines.append("")
lines.append(f"/N {N} def")
lines.append("N { 7 11 hot } repeat")
lines.append("")

with open(out, "w") as f:
    f.write("\n".join(lines))

print(f"wrote {out}: K={K} N={N} param_reads={2 * K * N}")
